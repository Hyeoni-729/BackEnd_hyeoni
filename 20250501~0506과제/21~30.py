# 28번 문자열 중앙 문자
## 문자열을 입력받아 중앙에 위치한 문자(들)를 출력하는 프로그램을 작성하세요.
## (홀수 길이면 가운데 1글자, 짝수 길이면 가운데 2글자)
a = input()
if len(a) % 2 == 1:
    b = a[len(a) // 2]
    print(f"중앙 문자 : {b}")
else:
    b = a[len(a) // 2 - 1:len(a) // 2 + 1]
    print(f"중앙 문자 : {b}")

# 29번 특수문자 제거
## 문자열에서 특수문자(., !, ? 등)를 제거하는 프로그램을 작성하세요.
## isalnum() : 알파벳(a-z, A-Z)과 숫자(0-9)로 이루어진 문자인지 확인
## isspace() : 공백 문자(' ', '\t', '\n' 등)를 포함시키기 위해 사용
## c for c in a : 문자열에서 각 문자를 하나씩 확인
## ''.join() : 조건을 만족하는 문자들만 이어 붙여 새로운 문자열을 만듬
a = input()
b = ''.join(c for c in a if c.isalnum() or c.isspace())
print(b)

