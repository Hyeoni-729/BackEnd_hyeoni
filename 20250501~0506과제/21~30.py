# 27번 문자열 패딩
## 문자열(a)과 원하는 길이(b)를 입력받아, 원하는 길이가 될 때까지 '*'로 오른쪽을 채우는 프로그램을 작성하세요.
## ljust(문자열 왼쪽은 그대로 두고, 지정된 길이만큼 오른쪽에 특정문자를 채워주는 함수 rjust는 반대로)
